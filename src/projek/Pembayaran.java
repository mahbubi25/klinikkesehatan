    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String nama = nama_box.getSelectedItem().toString();
        String alamat = alamat_box.getText();
        String telp = telp_box.getText();
        int biayatotal = Integer.parseInt(biaya_box.getText()) + Integer.parseInt(biayaobt.getText());
        String biaya3 = String.valueOf(biayatotal);
        
        Connection connect = koneksi.getKoneksi();
        //query untuk memasukan data
//        String query = "{CALL `inputDataPembayaran`('"+nama+"','"+tindakan+"','"+biaya+"','"+obat+"','"+biayaobat+"')}";
        try{
            //menyiapkan statement untuk di eksekusi
            String sql = "TRUNCATE TABLE pembayaran";
            PreparedStatement ps = (PreparedStatement) connect.prepareStatement(sql);
            ps.executeUpdate(sql);
//            JOptionPane.showMessageDialog(null,"Berhasil melakukan pembayaran");
            
        }catch(SQLException | HeadlessException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Gagal melakukan pembayaran!");
            
        }
        
        try{
            //menyiapkan statement untuk di eksekusi
            String sql = "INSERT INTO `pembayaran` (`id_pembayaran`, `nama`, `jenis_tindakan`, `biaya`, `obat`, `biaya_obat`) VALUES ('12', '"+nama_box.getSelectedItem()+"', '"+tindakan_box.getSelectedItem()+"', '"+biaya_box.getText()+"', '"+obt.getSelectedItem()+"', '"+biayaobt.getText()+"')";
            PreparedStatement ps = (PreparedStatement) connect.prepareStatement(sql);
            ps.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Berhasil melakukan pembayaran");
            
        }catch(SQLException | HeadlessException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Gagal melakukan pembayaran!");
            
        }finally{
            
            Kwitansi_Pembayaran kwitansi = new Kwitansi_Pembayaran();
            kwitansi.setNama(nama);
            kwitansi.setAlamat(alamat);
            kwitansi.setTelp(telp);
            kwitansi.setTotal(String.valueOf(biayatotal));
            kwitansi.tampilData();

            kwitansi.setVisible(true);
            this.dispose();
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

