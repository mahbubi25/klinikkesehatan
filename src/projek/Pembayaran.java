    private void nama_boxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nama_boxActionPerformed
        // TODO add your handling code here:
        if (nama_box.getSelectedItem() != "Pilih Pendaftar!"){
            String alamat = null,nama = null,telp = null, poli=null;
            String query = "SELECT * FROM pendaftaran WHERE nama='"+nama_box.getSelectedItem()+"'";
            try{
           Connection connect = koneksi.getKoneksi();//memanggil koneksi
           Statement sttmnt = connect.createStatement();//membuat statement
           ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
           
           while (rslt.next()){
                //menampung data sementara
                   
                     alamat = rslt.getString("alamat");
                     nama = rslt.getString("nama");
                     telp = rslt.getString("telp");  
                     poli = rslt.getString("poli");
            }
                //mengeset nilai yang ditampung agar muncul di table
                alamat_box.setText(alamat);
                telp_box.setText(telp);
                poli_box.setSelectedItem(poli);
                
    }catch(Exception e){
           System.out.println(e);
    }
        }else{
            alamat_box.setText(null);
                telp_box.setText(null);
                poli_box.setSelectedItem(null);
        }
    }//GEN-LAST:event_nama_boxActionPerformed

